def load_imagenet_classes():
    """Load ImageNet class names from file or URL."""
    import json
    from pathlib import Path
    
    # Check for local cache
    cache_path = Path(__file__).parent / "imagenet_classes.json"
    if cache_path.exists():
        with open(cache_path) as f:
            return json.load(f)
    
    # Download from standard source
    try:
        import urllib.request
        url = "https://raw.githubusercontent.com/anishathalye/imagenet-simple-labels/master/imagenet-simple-labels.json"
        with urllib.request.urlopen(url, timeout=10) as response:
            simple_labels = json.loads(response.read().decode())
        
        # Map indices to labels (ImageNet classes are 0-999)
        # We'll also need synset mapping - for now use simple approach
        return {str(i): label for i, label in enumerate(simple_labels)}
    except Exception:
        return {}


